void DNG_draw_walls(signed int x, signed int y, signed int a3)
{
	signed int nvf_width;
	signed int nvf_height;
	signed int width2;
	signed int height2;
	signed int flag;
	uint8_t *dst_ptr;
	uint8_t *ptr2;
	struct nvf_extract_desc nvf;

	flag = (a3 & 0x8000) ? 1 : 0;
	a3 &= 0x3fff;

	if ((a3 >= 8) && (a3 <= 13) && !g_dng_floor_tex) {
		a3 += 6;
	} else {
		if ((a3 >= 14) && (a3 <= 19) && !g_dng_floor_tex) {
			a3 -= 6;
		}
	}

	nvf.dst = dst_ptr = g_renderbuf_ptr + 0x7530;
	nvf.src = (uint8_t*)g_buffer9_ptr3;
	nvf.image_num = a3;
	nvf.width = &nvf_width;
	nvf.height = &nvf_height;
	nvf.compression_type = 3;
	process_nvf_extraction(&nvf);

	width2 = nvf_width;
	height2 = nvf_height;

	if ((x < 0) && ((x + width2) > 0)) {
		width2 += x;
		dst_ptr += __abs__(x);
		x = 0;
	}

	if ((y < 0) && ((y + height2) > 0)) {
		height2 -= y;
		dst_ptr += __abs__(y) * nvf_width;
		y = 0;
	}

	if ((x < 208) && (y < 135) && (x >= 0) && (y >= 0)) {

		if (x + width2 > 208) {
			width2 = 208 - x;
		}

		if (y + height2 > 135) {
			height2 = 135 - y;
		}

		ptr2 = g_renderbuf_ptr + 208 * y + x;

		if (!flag) {
			copy_solid(ptr2, dst_ptr, width2, height2, 208, nvf_width, 128);
		} else {
			copy_solid_permuted(ptr2, dst_ptr + width2 - 1, width2, height2, 208, nvf_width, 128);
		}
	}
}

